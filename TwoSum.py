#Two Sum
#问题描述
#Given an array of integers, find two numbers such that they add up to a specific target number.
#寻找和为定值的两个数,要求返回索引
#思路1:穷举,复杂度为O(N^2),超时
#思路2:构造hash,空间换时间
#思路3:先排序后指针两端扫描法,O(N log N)
class Solution:
    # @return a tuple, (index1, index2)

    #制作hash
    def twoSum1(self,num,target):
        l=len(num)
        reverse=[]
        for x in range(l):
            reverse.append(target-num[x])
        reverse=dict(zip(reverse,range(l)))
        for x in range(l):
            a = reverse.get(num[x])
            if a and a is not x:
                return (x+1,a+1)
        return (0,0)

    #先排序后指针两端扫描法
    def twoSum2(self,num,target):
        l=len(num)
        num2=list(num)
        num=sorted(num)
        x=0
        y=l-1
        while x<y:
            #print num
            s=num[x]+num[y]
            if s==target:
                break;
            else:
                if s<target:
                    x=x+1
                else:
                    y=y-1
        find=[]
        for i in range(l):
            if num[x]==num2[i] or num[y]==num2[i]:
                find.append(i+1)

        return tuple(find)




